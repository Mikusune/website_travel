                        Raja Ampat
                    </div>
                		<p>Jauh dari hiruk pikuk kota metropolitan, salah satu destinasi #DiIndonesiaAja istimewa ini menawarkan ketenangan dan kedamaian.&nbsp;</p>
                		<p>Raja Ampat, sebuah Kabupaten dan merupakan bagian dari Provinsi Papua Barat. Suguhan alam yang ditawarkan begitu mengagumkan, hingga bisa membuat wisatawan enggan pulang.<br>&nbsp;</p>
        				<img alt="" src="gambar/Raja ampat.jpg" ></br>
        				<h2>Keistimewaan Raja Ampat<br></h2>
        				<p>Kepulauan Raja Ampat berada di bagian paling barat Papua dan membentang di area seluas kurang lebih 4,6 juta hektar. Kabupaten Raja Ampat terdiri dari 4 pulau besar yaitu Pulau Waigeo, Batanta, Salawati dan Misool, dan 1.847 pulau-pulau kecil lainnya. Nah, nama Raja Ampat sendiri diyakini berasal dari legenda masyarakat setempat yang percaya bahwa zaman dahulu kala ada seorang wanita yang menemukan tujuh telur, empat telur tersebut menetas menjadi raja-raja yang berkuasa di empat pulau utama. Tersisa tiga lainnya, satu menjadi batu, satu menjadi wanita, dan satu lagi menjadi makhluk gaib atau hantu.Terlepas legenda yang dipercayai masyarakat setempat, keindahan yang disuguhkan oleh Raja Ampat merupakan fakta yang tak bisa diganggu gugat. <br></p>
        				<img alt="" src="gambar/ikan.jpg" ></br>
        				<p>Bagi Sobat Pesona yang gemar diving dan snorkeling, wilayah perairan Raja Ampat adalah salah satu destinasi diving terbaik di dunia. Menurut laporan sebuah organisasi sosial lingkungan internasional, The Nature Conservancy and Conservation International, sekitar 75% spesies karang di dunia hidup di kepulauan Raja Ampat. Destinasi ini memiliki kekayaan dan keunikan spesies yang tinggi dengan ditemukannya 1.318 jenis ikan, 699 jenis moluska (hewan lunak), dan 537 jenis terumbu karang. Rasakan sendiri sensasi menyelam dan bertemu dengan ragam jenis biota laut yang unik, seperti kuda laut kerdil (pygmy seahorse), ikan kelelawar, hingga dugong bisa kamu temukan di wilayah perairan Raja Ampat.<br></p>
        				<img alt="" src="gambar/penyelam.jpg" ></br>
        			    <p>Beberapa spot menyelam terbaik dan paling terkenal di Raja Ampat di antaranya&nbsp; adalah Kabui Passage, di sekitar Dermaga Pulau Arborek, Sauwandarek, Yenbuba, Dinding Friwen, dan banyak lagi! Tak heran ya, jika banyak sekali wisatawan mancanegara yang rela jauh-jauh datang ke Indonesia untuk menikmati pesona bawah lautnya yang memesona. Kamu jangan mau kalah, yuk, masukkan Raja Ampat ke daftar kunjunganmu nanti, Sobat Pesona!</p>
        			    <p>Tak hanya keindahan bawah lautnya yang luar biasa, pemandangan di atas permukaan juga tak kalah memesona. Panorama deretan pulau-pulau batu di tengah gradasi air biru terlihat begitu surgawi saat dilihat dari atas bukit. Siapkan fisik yang kuat dan lakukan trekking ke atas puncak bukitnya untuk melihat pemandangan indah seperti ini, ya!</p>
        			    <img alt="" src="gambar/RA_Pianemoisland_indtravel.jpg" ></br>
        			    <h2>Desa Wisata di Raja Ampat</h2>
        			    <p>Disamping pemandangan alamnya yang memukau, Raja Ampat juga menawarkan keramahan masyarakat setempat yang akan membuat Sobat Pesona betah. Selain menjelajahi alamnya, jangan lupa untuk menyelami budaya setempat, ya! Sobat Pesona bisa mengunjungi sekaligus menginap di Desa Wisata Arborek.&nbsp;</p>
        			    <img alt="" src="gambar/rajaampat2.jpg" ></br>
        			    <p>Ada banyak sekali pilihan penginapan serta atraksi wisata yang bisa Sobat Pesona nikmati selama di desa wisata ini, seperti melihat pembuatan kerajinan anyaman khas Arborek yang sudah terkenal hingga mancanegara, hingga mencicipi berbagai macam kuliner tradisional khas Papua, seperti papeda atau sinole.<br></p>
        			    <h2>Yuk, ke Raja Ampat!<br></h2>
        			    <p>Untuk mencapai kepulauan Raja Ampat, Sobat Pesona pertama-tama harus mengambil penerbangan dari Jakarta atau kota-kota besar lainnya ke Sorong, Papua. Sesampainya di Bandara Sorong, kamu bisa melanjutkan perjalanan ke Raja Ampat via laut.</p>
        			    <p>Ada tiga pintu masuk sekaligus lokasi pengecekan menuju ke Raja Ampat, yaitu Pelabuhan Falaya di Wasai, Pelabuhan Yellu di Pulau Misool, dan Bandara Marindi di Pulau Waigeo.&nbsp;</p>
        			    <p>Wah, kemudahan akses yang diberikan bikin kita nggak sabar untuk segera menyapa keajaiban di Raja Ampat ya, Sobat Pesona!</p>
        			    <p>Tetap disiplin menerapkan protokol kesehatan di mana pun Sobat Pesona berada! Selalu gunakan masker, mencuci tangan secara berkala, dan menjaga jarak sebagai bentuk kepedulian terhadap diri sendiri dan orang-orang sekitar!</p>
