                        Pantai Likupang: Mempersembahkan Keindahan Indonesia yang Tak Tertandingi
                    </div>
                		<p>Sulawesi Utara merupakan provinsi di Indonesia yang terkenal dengan destinasi wisata alamnya yang menakjubkan. Pantai, pegunungan, serta hutan yang rimbun wajib Sobat Pesona kunjungi ketika berada di provinsi tersebut. Salah satu tempat terbaik yang dapat Sobat Pesona temukan di sini adalah Pantai Likupang. Pantai ini terletak di bagian paling utara pulau Sulawesi dan hanya berjarak 48 km dari kota Manado, tepatnya berada di kabupaten Likupang.</br></p>
                    <p>Sejak pertama kali Sobat Pesona memandang Pantai Likupang maka pasti mata akan dibuat terpana oleh hamparan pantai berpasir putih yang indah dan luas. Tidak hanya itu, Pantai Likupang juga memiliki air laut yang sangat jernih dan menawan, serta menawarkan berbagai tempat dan kegiatan wisata lainnya. Lebih lanjut, Sobat Pesona juga bisa menikmati pemandangan bawah laut  dengan menyelam langsung ke dalam lautan biru yang pasti akan membuat anda takjub. Likupang juga memiliki beberapa tujuan wisata lainnya seperti Pantai Pal dan Pantai Pulisan. Tidak jauh dari Likupang terdapat beberapa Pulau yang juga memiliki keindahan bawah laut yang luar biasa, yaitu Pulau Lihaga dan Pulau Gangga.<br></p>
                    <p>Bila Sobat Pesona berangkat dari Manado, untuk mencapai Likupang, Sobat Pesona dapat menggunakan transportasi umum Bus dari terminal Paal. Bus tersebut akan membawa Sobat Pesona langsung ke Terminal Bus Likupang. Jika Sobat Pesona berangkat bersama keluarga atau teman-teman, disarankan untuk membawa kendaraan sendiri atau menyewa mobil dari rental, karena pantai ini berjarak cukup jauh dari pusat kota.
                    </p>
                    <img alt="" src="gambar/Article_Likupang.jpg"></br>
                    <h2><b>Destinasi Wisata Utama di Kawasan Likupang</b></h2>
                    <p>Pantai berpasir putih, dan air laut yang biru dan menyegarkan dibingkai dengan perbukitan hijau di kejauhan tentunya akan membuat Sobat Pesona merasa tenang. Ragam pilihan aktivitas yang dapat dilakukan di sana, bisa membuatmu betah dan pasti ingin tinggal lebih lama. Sobat Pesona bisa menjajal berbagai kegiatan mulai dari snorkeling, menyelam, dan juga memancing di area Pantai Likupang.</p>	
                    <p>Perairan jernih ini pernah mengejutkan penduduk setempat dan World Wildlife Fund Indonesia, karena Penyu Hijau ditemukan di daerah tersebut pada tahun 2007 . Kehadiran spesies langka ini merupakan tanda bahwa ekosistem laut Sulawesi Utara sangat terjaga. Karena perairannya yang jernih, tempat ini menjadi favorit bagi para penyelam dan pecinta snorkeling untuk menikmati keindahan terumbu karang yang menakjubkan di bawah air.</p> 
                    <p>Paradise Watersport di Casabaio Paradise Resort adalah objek wisata yang sangat difavoritkan di dekat pantai. Bagaikan acara “Ninja Warrior” dimana peserta ditantang untuk melompat di trampolin yang tepat berada diatas permukaan air! Namun perbedaannya jika Sobat Pesona terjatuh di sini maka akan langsung menyelam ke perairan biru yang indah. Tak hanya melompat-lompat, Sobat Pesona juga dapat memilih berbagai kegiatan olahraga air seperti mengendarai Jet Ski, Banana Boat, dan lainnya.</p>
                    <h2><b>Destinasi Menarik Di Sekitarnya</b></h2>
                    <p>Terdapat beberapa pantai lainnya di sekitar Pantai Likupang yang perlu Sobat Pesona pertimbangkan untuk dikunjungi. Pantai Pal, Pantai Pulisan dan pantai-pantai di sepanjang teluk yang sering dijadikan tempat untuk snorkeling maupun menyelam, oleh wisatawan lokal namun terutama wisatawan mancanegara. Keindahan pantai-pantai di sini tak kalah dengan keindahan Bunaken. Jika Sobat Pesona ingin memulai petualangan baru, kunjungilah Pulau Lihaga dan Pulau Gangga yang memiliki pemandangan bawah laut yang tak kalah menakjubkan.</p> 
