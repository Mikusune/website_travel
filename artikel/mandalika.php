                        Mandalika: Kepingan Surga Budaya dan Alam Tersembunyi di Lombok, NTB
                    </div>
                		<p>Membentang luas dari Sabang hingga Merauke, mengulas pesona destinasi wisata #DiIndonesiaAja emang enggak akan ada habisnya. Bener enggak, Sobat Pesona? Nah, salah satu kepingan surga tersembunyi di Bumi Ibu Pertiwi ini bisa ditemukan di NTB. Tepatnya di Lombok Tengah dan jadi primadona para pencinta wisata olahraga, apalagi kalau bukan Mandalika!</br></p>
                    <p>Ternyata nih, bukan cuma ikon Sirkuit Mandalika kelas dunia yang bakal memanjakan Sobat Pesona saat main ke daerah yang punya kain tenun indah ini. Tapi, ada juga destinasi wisata alam, kuliner, hingga kekayaan budaya. Yuk, cari tahu info selengkapnya di bawah ini!</p>
                    <img alt="" src="gambar/merese-hill.jpg"></br>
                    <h2><b>Sejarah</b></h2>
                    <p>Ada sebuah legenda atau cerita rakyat terkenal di Mandalika, yaitu legenda Putri Mandalika yang jadi asal-usul nama daerah tersebut. Dirinya merupakan seorang putri cantik dari tokoh suku Sasak terkemuka dan pesonanya menghipnotis semua lelaki, bahkan hingga luar Lombok. Lalu, suatu ketika Putri Mandalika kewalahan menerima banyak pinangan para pelamar dan memutuskan untuk menenangkan diri dengan bersemedi. Keesokan harinya, Putri Mandalika mengumpulkan seluruh para lelaki yang menyukainya di Bukit Seger.<br></p>
                    <p>Alih-alih memilih satu lamaran, ternyata putri jelita tersebut justru menghempaskan badannya ke dalam gulungan ombak Pantai Seger dan menghilang tanpa ada jejak. Uniknya, setelah sang putri menghilang, Pantai Seger kemudian dipenuhi oleh cacing laut berwarna-warni. Masyarakat lokal pun mempercayai cacing laut itu sebagai bentuk lain dari Putri Mandalika.</p>
                    <img alt="" src="gambar/sejarah-mandalika.jpg"></br>
                    <p>Dengan legenda Putri Mandalika, hadirlah perayaan upacara Bau Nyale yang selalu diadakan setiap tahunnya dan menarik perhatian wisatawan lokal hingga mancanegara. Bau Nyale sendiri merupakan ritual mencari cacing laut yang diyakini sebagai jelmaan Putri Mandalika yang menghilang setelah terjun dari atas Bukit Seger ke Pantai Seger. Sobat Pesona juga bisa melihat langsung monumen Putri Mandalika di Pantai Seger lho!<br></p>
                    <h2><b>Destinasi</b></h2>
                    <img alt="" src="gambar/laut mandalika.jpg"></br>
                    <p>Pilihan destinasi wisata di Mandalika enggak perlu diragukan lagi nih, Sobat Pesona. Mandalika punya banyak spot menarik nan instagenic dan bikin momen liburan makin berkesan. Pilihan pantai aduhai bisa Sobat Pesona temukan di Pantai Seger, Pantai Serenting, Pantai Mawi, Pantai Kuta Mandalika, dan Pantai Tanjung Aan. Sedangkan buat menjelajahi budaya yang kaya makna, coba kunjungi Desa Sade dan Desa Ende buat melihat proses pembuatan kain tenun songket, tradisi, hingga tarian adat yang super menarik.<br></p>
                    <p>Buat urusan akomodasi, Sobat Pesona bisa memilih tempat sesuai kebutuhan dan keuangan masing-masing. Mulai dari homestay yang nyaman dan lekat dengan nuansa lokal hingga hotel dan resor mewah kelas internasional, Mandalika punya rekomendasi akomodasi unggulan yang bikin betah!<br></p>
                    <h2><b>Kuliner</b></h2>
                    <img alt="" src="gambar/kuliner-mandalika.jpg"></br>
                    <p>Suka wisata kuliner? Jangan khawatir Sobat Pesona, karena Mandalika punya aneka makanan lokal hingga hidangan internasional! Beberapa rekomendasi kuliner khas Mandalika yang menggugah selera antara lain, sate bulayak yang terbuat dari sate berbahan daging sapi bermandi bumbu rempah perpaduan kacang sangrai serta santan dan dihidangkan bersama lontong daun aren khas Lombok. Ada juga sayur ares dari pelepah pisang, nasi balap puyung dengan lauk pauk super lengkap, serta sambal beberuk terung asam nan segar!<br></p>
                    <p>Yang pasti, jangan lupa juga mencicipi bintang utama dari santapan khas NTB kesukaan para wisatawan nih Sobat Pesona. Betul, ayam taliwang! Kombinasi bumbu pedas dengan rempah yang meresap dan sensasi bakar yang menggoda, jadi definisi makan sempurna di Mandalika.<br></p>
                    <h2><b>Cara Menuju</b></h2>
                    <p>Untuk menuju ke Mandalika ada beberapa cara yang dilakukan di antaranya melalui jalur laut dan jalur udara, serta umumnya ditempuh dari Lombok.<br></p>
                    <p><b>Jalur Udara:</b></p>
                    <p>Kalau Sobat Pesona memilih jalur udara, Sobat Pesona bisa ke Bandar Udara Internasional Lombok Zainuddin Abdul Madjid. Bandara ini berjarak kurang lebih 27 km dan membutuhkan waktu 1 jam perjalanan untuk bisa sampai ke Mandalika.</p>
                    <p><b>Jalur Laut:</b></p>
                    <p>Untuk jalur laut sendiri, Sobat Pesona bisa menyeberang menggunakan kapal Feri yang tersedia dari Pelabuhan Padangbai, Bali yang jaraknya sekitar 60 km dari Ngurah Rai dengan tujuan Pelabuhan Lembar, Lombok. Sesampainya di Pelabuhan Lembar, Sobat Pesona butuh waktu sekitar 45 menit dengan jarak 22 km untuk sampai ke Mandalika.</p>
